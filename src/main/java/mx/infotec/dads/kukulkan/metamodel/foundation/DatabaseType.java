package mx.infotec.dads.kukulkan.metamodel.foundation;

/**
 * DatabaseType
 * 
 * @author Daniel Cortes Pichardo
 *
 */
public enum DatabaseType {
    /**
     * Indicates that the Database is SQL implemented by Mysql
     */
    SQL_MYSQL,

    /**
     * Indicates that the Database is SQL implemented by Oracle.
     */
    SQL_ORACLE,

    /**
     * Indicates that the Database is NO SQL
     */
    NO_SQL_MONGODB,

}
